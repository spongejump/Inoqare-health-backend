const ErrorResponse = require("../utils/errorResponse");
const asyncHandler = require("../middleware/async");
const uploadFile = require("../middleware/upload");
const fs = require("fs");

//Devlopment 
const baseUrl = 'http://localhost:5000/files/';

/* Production */
